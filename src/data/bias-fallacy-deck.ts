const withSources = (cards: RawStudyCard[]): StudyCard[] =>
  cards.map(({ sourceSlugs = [], ...card }) => {
    const merged = [
      ...(card.kind === 'bias' ? biasDefaultSourceSlugs : fallacyDefaultSourceSlugs),
      ...sourceSlugs,
    ]

    return {
      ...card,
      sources: Array.from(new Set(merged)).map((slug) => sourceRegistry[slug]),
    }
  })

